 private void tampil(){
        try{
            int baris =tabledistributor.getRowCount();
            for (int i =0; i < baris; i++){
                ModelDataDistributor.delete(0,baris);
            }
            String SQL = "select * from tbldistributor";
            ResultSet rs = conn.getData(SQL);
            while (rs.next()) {
                DataDistributor distributor = new DataDistributor();
                distributor.setIdDistributor (rs.getString(1));
                distributor.setNamaDistributor (rs.getString(2));
                distributor.setAlamat (rs.getString(3));
                distributor.setKotaAsal (rs.getString(4));
                distributor.setEmail (rs.getString(5));
                distributor.setTelepon (rs.getString(6));
                ModelDataDistributor.add(distributor);
            }
        } catch (SQLException ex) {
            System.err.print(ex);
            }
    }
