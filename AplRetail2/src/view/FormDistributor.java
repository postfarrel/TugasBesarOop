    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
         String KJ=iddistributor.getText();
        String J=namadistributor.getText();
        String A=alamat.getText();
        String K=kotaasal.getText();
        String E=email.getText();
        String T=telpon.getText();
        if ((KJ.isEmpty()) | (J.isEmpty()) | (A.isEmpty()) | (K.isEmpty()) | (E.isEmpty()) | (T.isEmpty())){
            JOptionPane.showMessageDialog(null,"data tidak boleh kosong,"+
                "silahkan dilengkapi");
            iddistributor.requestFocus();
        }else{
            try{
                String SQL = "update tbldistributor set "
                + " namadistributor ='"+namadistributor.getText()+"',"
                + " alamat ='"+alamat.getText()+"',"
                + " kotaasal ='"+kotaasal.getText()+"',"
                + " email ='"+email.getText()+"',"
                + " telepon ='"+telpon.getText()+"' where"
                + " iddistributor = '"+iddistributor.getText()+"'";

                conn.query(SQL);
                tampil();
                JOptionPane.showMessageDialog(null, iddistributor.getText()+
                    "Berhasil Diubah");
                BersihData();
                SetEditOff();
                add.setEnabled(true);
            } catch (Exception ex){
                System.err.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_updateActionPerformed

